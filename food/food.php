								<a href="#"><h3>&nbsp;Cranberry Pancakes</h3></a><br>
								<h4>&nbsp;&nbsp;INGREDIENTS</h4>
							&nbsp;&nbsp;•&nbsp;&nbsp;1/2 cup fresh cranberries<br>
&nbsp;&nbsp;•&nbsp;&nbsp;1/4 cup all-purpose flour<br>
&nbsp;&nbsp;•&nbsp;&nbsp;2 tablespoons plus 2 teaspoons whole-wheat flour<br>
&nbsp;&nbsp;•&nbsp;&nbsp;1 tablespoon yellow cornmeal<br>
&nbsp;&nbsp;•&nbsp;&nbsp;1 tablespoon sugar<br>
&nbsp;&nbsp;•&nbsp;&nbsp;1/2 teaspoon baking powder<br>
&nbsp;&nbsp;•&nbsp;&nbsp;1/8 teaspoon salt<br>
&nbsp;&nbsp;•&nbsp;&nbsp;1/8 teaspoon ground nutmeg, or 1/4 teaspoon vanilla extract<br>
&nbsp;&nbsp;•&nbsp;&nbsp;6 tablespoons nonfat milk<br>
&nbsp;&nbsp;•&nbsp;&nbsp;2 tablespoons pasteurized egg substitute, such as Egg Beaters<br>
&nbsp;&nbsp;•&nbsp;&nbsp;1 1/2 teaspoons walnut or canola oil<br>

							

								
								

								
							</p>
							<p>
							<h4>&nbsp;&nbsp;PREPARATION</h4>
							1.	Bring 2 inches of water to a boil in a small saucepan. Add cranberries; boil for 2 minutes. Drain and cool for 5 minutes.<br>
2.	Meanwhile, whisk all-purpose flour, whole-wheat flour, cornmeal, sugar, baking powder, salt and nutmeg (if using) in a large bowl.<br>
3.	Whisk milk, egg substitute, oil and vanilla (if using) in a small bowl until combined.<br>
4.	Coarsely chop the cranberries; stir into the milk mixture. Stir the milk mixture into the dry ingredients just until combined.<br>
5.	Coat a griddle or large nonstick skillet with cooking spray; heat over medium heat. Using 1/4 cup of batter for each pancake, cook 2 pancakes at a time until bubbles dot the surface, 2 to 3 minutes. Flip and continue cooking until browned, 1 to 2 minutes more. Repeat with the remaining batter.<br>

							
