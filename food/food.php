								<a href="#"><h3>&nbsp;Herb & Onion Frittata</h3></a><br>
								<h4>&nbsp;&nbsp;INGREDIENTS</h4>
								&nbsp;&nbsp;•&nbsp;&nbsp;1 cup diced onion<br>
&nbsp;&nbsp;•&nbsp;&nbsp;1/4 cup plus 1 tablespoon water, divided<br>
&nbsp;&nbsp;•&nbsp;&nbsp;1 teaspoon extra-virgin olive oil<br>
&nbsp;&nbsp;•&nbsp;&nbsp;1/2 cup liquid egg substitute, such as Egg Beaters<br>
&nbsp;&nbsp;•&nbsp;&nbsp;2 teaspoons chopped fresh herbs, or 1/2 teaspoon dried<br>
&nbsp;&nbsp;•&nbsp;&nbsp;1/8 teaspoon salt<br>
&nbsp;&nbsp;•&nbsp;&nbsp;1/8 teaspoon freshly ground pepper<br>
&nbsp;&nbsp;•&nbsp;&nbsp;2 tablespoons farmer's cheese, or reduced-fat ricotta<br>

								

								
								

								
							</p>
							<p>
							<h4>&nbsp;&nbsp;PREPARATION</h4>
							Bring onion and 1/4 cup water to a boil in a small nonstick skillet over medium-high heat. Cover and cook until the onion is slightly softened, about 2 minutes. Uncover and continue cooking until the water has evaporated, 1 to 2 minutes. Drizzle in oil and stir until coated. Continue cooking, stirring often, until the onion is beginning to brown, 1 to 2 minutes more.<br>
	Pour in egg substitute, reduce heat to medium-low and continue cooking, stirring constantly with a heatproof rubber spatula, until the egg is starting to set, about 20 seconds. Continue cooking, lifting the edges so the uncooked egg will flow underneath, until mostly set, about 30 seconds more.<br>
	Reduce heat to low. Sprinkle herbs, salt and pepper over the frittata. Spoon cheese on top. Lift up an edge of the frittata and drizzle the remaining 1 tablespoon water under it. Cover and cook until the egg is completely set and the cheese is hot, about 2 minutes. Slide the frittata out of the pan using the spatula and serve.<br>
						
