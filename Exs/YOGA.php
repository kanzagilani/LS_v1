   <h4>Chair Pose</h4>
   Also known as the fierce asana, this is beneficial to tone down your calves. However, do not practice the pose if you have a knee injury or arthritis. This pose is also beneficial to a better functioning heart, abdominal organs, and diaphragm.
<br>
<li>Stand straight with back erect</li>
<li>Spread out your legs in alignment with your shoulder width.</li>
<li>Stretch out your hands without bending the elbows to your front</li>
<li>Slowly, bend your knees while pushing the pelvis down. It should resemble a person sitting on a chair.</li>
<li>Keep your spine erect while ensuring that your hands are parallel to the ground.</li>
<li>Breathe normally and hold the position for about 30 seconds</li>
<li>Exhale slowly, relax your hands, and gently come back to the initial position.<li>
