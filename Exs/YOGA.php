   <h4>Raised Foot Pose</h4>
   This pose works exclusively on your lower abdominal region, hips, and thighs. Along with rendering a toned lower body, this pose is known to be one of the most sought after postpartum weight loss poses. The pose is also known to enhance the functioning of the liver, lungs, heart, intestines, and pancreas. Women, after delivery, can indulge in this pose to alleviate the back pain and to augment their digestive power.
<br>
<li>Lie down in supine position with knees and feet kept together. Breathe normally.</li>
<li>Exhaling slowly, arch the back while tilting your head backward to touch the floor.</li>
<li>Keep the arms on either side of the body, palms facing your body. Breathe normally.</li>
<li>Stretch as much as possible and inhaling deeply, lift both of your legs to make a 45-degree angle with the ground</li>
<li>Hold the pose for a period of 30 to 60 counts.</li>
<li>Now, slowly bring the legs up to make a 90-degree angle with the floor. Continue to hold, breathing normally, for 30 counts.</li>
<li>Inhale deeply and slowly bring your legs back to initial position – the supine position.

</li>
